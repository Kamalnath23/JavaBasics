package arraystring;

public class Char {
	public static void main(String[] args) {
		for(char c =65;c<91;c++) {
			System.out.print(c+" ");
		}
		
		System.out.println();
		for(char c=97;c<123;c++) {
			System.out.print(c+" ");
		}
		
		System.out.println();
		char c=91;
		while(c<97) {
			System.out.print(c+ " ");
			c++;
		}
		
		System.out.println();
		for(char c1=0;c1<=64;c1++) {
			System.out.print(c1+" ");
		}

	}

}
